int	main(int argc, char **argv)
{
	char c;

	c = 'z'; 
	while (c >= 'a')
	{
		if (c % 2)
		{
			c -= 32;
			write(1, &c, 1);
			c += 32;
		}
		else
			write(1, &c, 1);
		c--;
	}
	write(1, "\n", 1);
}
