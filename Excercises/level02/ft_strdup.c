char	*ft_strdup(char *src)
{
	int		i;
	char	*src2;

	i = 0;
	while (src[i] != '\0')
		i++;
	src2 = malloc(sizeof(char) * (i + 1));
	i = 0;
	while (src[i] != '\0')
	{
		src2[i] = src[i];
		i++;
	}
	src2[i] = '\0';
	return (src2);
}
